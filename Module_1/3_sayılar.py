# Sayılar (Numbers) : int, float, complex

a = 3
b = 10.5

# matematiksel işlemler gerçekleştirebilirsin.
print(a * 3)
print(a / 7)
print(a * b / 10)
print(a ** 2)

# Tipleri değiştirmek
print(int(b))
print(float(a))
print(int(a * b / 10))

# Değişkene atayarak da aynı işlemi gerçekleştirebiliriz.
c = a * b ** 2 / 3
print(int(c))