# Fonksiyonların Statement/Body Bölümü

# def function_name(parameters/arguments):
#     statements (function body)

def say_hi(string):
    print(string)
    print("Hi")
    print("Hello")

say_hi("miuul")

def multiplication(a, b):
    c = a * b
    print(c)

multiplication(5, 4)

list_store = []

def add_element(a, b):
    c = a * b
    list_store.append(c)
    print(list_store)

add_element(38, 2)
add_element(36, 4)