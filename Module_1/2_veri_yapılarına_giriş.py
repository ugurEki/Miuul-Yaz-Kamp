##########################################
# Veri yapılarına giriş ve hızlı özet
##########################################

# Sayılar : integer
x = 46
print(type(x))

# Sayılar : float
x = 10.3
print(type(x))

# Sayılar : complex
x = 2j + 1
print(type(x))

# String
x = "Hello ai era"
print(type(x))

# Boolean
x = True
x2 = False
print(type(x), type(x2))
print(1 == 2)
print(type(1 == 2))

# Liste 
x = ["btc", "eth", "xrp"]
print(type(x))

# Sözlük (dictionary)
x = {"name": "Peter", "Age": 36}
print(type(x))

# Tuple (demet)
x = ("python", "ml", "ds")
print(type(x))

# Set
x = {"python", "ml", "ds"}
print(type(x))

# Not : Liste, tuple, set ve dictionary veri yapıları aynı zamanda Python Collections(Arrays) olarak geçmektedir.




